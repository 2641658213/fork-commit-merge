def function(numbers):
    # Printing the list of numbers
    print("List of numbers:", numbers)

    # Calculating and printing the sum of the numbers
    total_sum = sum(numbers)
    print("Sum of the numbers:", total_sum)


# List of the first 10 natural numbers
numbers_list = list(range(1, 11))

# Calling the function with the list of natural numbers
function(numbers_list)
