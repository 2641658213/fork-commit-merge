#include <stdio.h>

int main() {
    int num1, num2;

    // Use & to get the addresses of num1 and num2
    scanf("%d %d", &num1, &num2);

    // Use %d twice to print two integers
    printf("%d + %d = %d\n", num1, num2, num1 + num2);

    return 0;
}


